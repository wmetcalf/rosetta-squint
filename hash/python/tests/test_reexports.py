# --- crop_resistant_hash + ImageMultiHash via re-export ---

def _multihash_cases() -> list[tuple[str, str]]:
    g = _load_goldens()
    algo = g["algorithms"].get("crop_resistant_hash")
    if not algo:
        return []
    return [(fix, sizes["default"]) for fix, sizes in sorted(algo["fixtures"].items())]


@pytest.mark.parametrize("fixture,expected_hex", _multihash_cases(),
                          ids=lambda v: str(v))
def test_reexport_crop_resistant_hash_matches_goldens(fixture: str, expected_hex: str):
    """Verifies that imagehash.crop_resistant_hash via the re-export still
    produces what's in goldens.json. If this fails after an `imagehash`
    upgrade, the goldens are stale and need regeneration."""
    img = Image.open(FIXTURES_DIR / fixture)
    multihash = rih.crop_resistant_hash(img)
    assert str(multihash) == expected_hex


def test_imagemultihash_subtract_returns_float():
    """Documented invariant: ImageMultiHash distance is float, not int."""
    img = Image.open(FIXTURES_DIR / "peppers.png")
    a = rih.crop_resistant_hash(img)
    b = rih.crop_resistant_hash(img)
    d = a - b
    assert isinstance(d, float), f"distance is {type(d).__name__}, expected float"
    assert d == 0.0, "identical images should have distance 0.0"


def test_hex_to_multihash_round_trip():
    img = Image.open(FIXTURES_DIR / "peppers.png")
    mh = rih.crop_resistant_hash(img)
    s = str(mh)
    restored = rih.hex_to_multihash(s)
    assert str(restored) == s, "hex round-trip failed"
